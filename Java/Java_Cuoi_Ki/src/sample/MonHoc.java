package sample;

public class MonHoc {
    public String MaMH;
    public String TenMH;

    public MonHoc() {
    }

    public MonHoc(String maMH, String tenMH) {
        MaMH = maMH;
        TenMH = tenMH;
    }

    public String getMaMH() {
        return MaMH;
    }

    public void setMaMH(String maMH) {
        MaMH = maMH;
    }

    public String getTenMH() {
        return TenMH;
    }

    public void setTenMH(String tenMH) {
        TenMH = tenMH;
    }
}
